#Circle Involute
from sympy import symbols, cos, sin, pi
from sympy.plotting import plot_parametric

a = 2
t = symbols('t')
x = a * (cos (t))
y = a * (sin (t))

xi = a * (cos(t) + t * sin(t))
yi = a * (sin(t) - t * cos(t))

#plot_parametric((x, y, (t, 0, 6*pi)))
plot_parametric(
    (x, y, (t,2 , 6* pi)),
    (xi, yi, (t, 0 , 2* pi))
    #show=True, title='Curvas Param√©tricas'
)

